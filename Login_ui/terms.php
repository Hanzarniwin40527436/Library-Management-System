            <h2 class="content-element__title">Library Services Agreement</h2>
            <p><b>1. Special Cases </b>The Library may provide access to services in different circumstances, including
                services for minors, and services to non-members who participate or attend Library programs or
                events:
            </p>
            <p><b>1.1 Access by Minors:</b> Children under the age of 18 and others lacking in capacity require the
                consent
                of
                a parent or guardian. In this case, the person named as the Guardian during registration (the
                “Guardian”) must provide consent to the Library. All references in this Library Services
                Agreement
                to
                Member are deemed to be references to “the Member and the Guardian, jointly and severally”, and
                the
                Guardian agrees that the Guardian remains liable and bound by the Library Services Agreement as
                though
                named as a Member.</p>
            <p><b>2. Unrestricted Internet Access:</b> Members will be able to participate in all online events,
                programs or
                other online Library Services. Internet access is not filtered.</p>
            <p><b>3. Communication with Minors</b> Once an Account is created, the Library will send communications as
                follows:
                for any minor who is under the age of 13 years, all Library communications will be through the
                Guardian;
                for any minor who is 13 or older, the Library may communicate directly with the Member regarding any
                resource requests, programs, event registration, or other Library Services, subject to the access
                permissions associated with that Account.</p>

            <p><b>4. Accuracy of Registration</b> The Member represents and warrants that
            <p>(a) all personal information
                provided
                by the Member to the Library for the purposes of creating, updating or maintaining the Account is
                correct and current</p>
            <p>(b) the Member will notify the Library in a timely manner of changes in
                personal information. The Member agrees to provide verification of personal information to the
                Library
                from time to time upon request by the Library. The Library may request the verification of personal
                information before the Account is granted, or as reasonably required under applicable Library
                policies,
                or in response to access requests. The Member’s failure to provide verification of personal
                information
                will result in the denial or suspension of the Account and of all rights and privileges granted by
                the
                Library to the Member.</p>
            </p>
            <p><b>5. Amendments </b>The Library may from time to time amend the Library Services Agreement or any code
                of
                conduct, policy or bylaw by publishing amendments on the Library’s website. Upon posting amendments,
                the
                updated Library Services Agreement, code of conduct, policy or bylaw is deemed amended and is
                immediately effective for all future actions of the Member. At all times, it is the responsibility
                of
                the Member to verify the current governing Library Services Agreement or code of conduct, policy or
                bylaw; the Library has no obligation to bring any amendment in the Library Services Agreement or any
                code of conduct, policy or bylaw to the attention of the Member in order for the amendment to be
                effective. Use of the Library Services after amendment constitutes acceptance of the amended
                version.
            </p>
            <p><b>6. Borrowing books</b></p>
            <p>a. As part of the Library Services, the Member is permitted to borrow reading materials, audio materials,
                audio-visual materials and access other content and materials (collectively, “Materials”) from the
                Library in accordance with and subject to any limitations imposed from time to time by the Library
                respecting borrowing of and access to Materials.
            <p>b. The Member may also have access to third-party electronic materials licensed by the Library. It is the
                responsibility of the Member to read and accept the terms of use for third-party licensed materials. If
                the Member chooses not to accept the terms of use for third-party licensed materials, the Member will
                not have access to these materials.</p>
            <p>c. The Member may have access to third-party online services licensed by the Library. It is the
                responsibility of the Member to read and accept the terms of use for third-party licensed services. If
                the Member chooses not to accept the terms of use for third-party licensed services, the Member will not
                have access to these services. Members will be alerted if Library Services are using a third-party
                online service.</p>
            </p>
            <p>d. While the Library may, as a courtesy, remind the Member about due dates of borrowed Materials, the
                Member is responsible for all Fees associated with overdue items, even if the Library has not contacted
                the Member to provide information about due dates of Materials borrowed by the Member.</p>
            <p>e. The Member may allow other Members to borrow items and place and pick up holds on the Member’s
                Account. Members given privileges to borrow and place and pick up holds on another Member’s account will
                be identified as having proxy access to the other Member’s Account. Members with proxy access to another
                Member’s account may have access to personal information on the account, but may not add or remove proxy
                access or change any personal information on that Member’s Account. Giving others proxy access to an
                Account does not relieve the Member of responsibility for late fees, lost items or other charges on the
                Account.


            <p><b>7. One Account Only</b> The Member shall have no more than one Account. The Member agrees not to
                generate
                multiple Accounts by providing the Library with multiple e-mail addresses and/or false, misleading
                or
                alternative personal information to be associated with distinct Accounts. If the Member does have
                multiple Accounts, the Member agrees that the Library may consolidate the Member’s multiple Accounts
                into a single Account. The Member agrees to pay all Fees owed to the Library in the Member’s
                multiple
                Accounts. The Member agrees that Fees owing on the Member’s multiple Accounts will be combined upon
                consolidation of the Member’s multiple Accounts into one Account.</p>
            </p>
            <p><b>8. Use of Personal Information</b></p>
            <p>a. The Library may share personal information collected from the Member with third-party service
                providers for the purpose of delivering Library Services, and other libraries where the Member has
                chosen to participate in a reciprocal borrowing arrangement such as the Alberta Library Card or the
                Alberta Wide Borrowing Card.
            <p>b. Unless the Member has withdrawn consent, the Library may use the Member’s personal information to
                contact the Member to inform members of upcoming due dates on Library materials and to share news about
                Library Services and resources, upcoming events and programs, and opportunities to support the Library.
            </p>
            <p>c. If the Member has consented to being contacted by the Calgary Public Library Foundation for
                fundraising purposes, the Library may disclose the Member’s personal information to the Calgary Public
                Library Foundation.</p>
            </p>
            <p>d. The Library will use aggregated and anonymized data (including demographic and other data which is
                detached from any personal identifying information). If the Member has any questions about the
                collection and use of the Member’s personal information, the Member may contact the Library’s Privacy
                Officer as listed on the Library’s website.</p>

            <p><b> 9. Entire Agreement </b>The Library Services Agreement is the entire agreement between the Library
                and
                the
                Member regarding the use of Library Services and the Account.</p>
